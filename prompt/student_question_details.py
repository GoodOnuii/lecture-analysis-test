"여기에서 그 X 길이를 구할 때 구하는 방법은 나왔는데 식을 세워서 거기서 어떻게 보여야 될지를 모르겠어요."
"그 닮음을 어떤 거랑 어떤 거랑 했을 땐 되고 어떤 거랑 어떤 거랑 했을 땐 안 되는데, 뭐랑 했을 땐 되는지 모르겠어요."
"여기서 티하고 티가 그 성염색체가 될 수 있는 거예요?"
"근데 가로 쳐서 아직 바꾸지 않은 거라 아직 마이너스 아니에요?"
"근데 왜 곱하기를 하는 거예요?"
"합성수의 뜻을 알려주실 수 있어요?"
"네, 그러면 얘랑 얘 또 2대1이 돼요?"
